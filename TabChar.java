    public class TabChar  
    {
            public static void main(String args[]) 
            {    
                char tab [] = new char[10];
                for (int i = 0; i < 10; ++i) {
                    tab[i] = 'i';
                    System.out.print(tab[i] + "" );
                    i++;
                }
            } 
        }
